package cn.edu.npu.sean.spider.bean;

public class UserUrl {
	private String snsInfoUrl;
	private String infoUrl;
	private long uid;
	public String getSnsInfoUrl() {
		return snsInfoUrl;
	}
	public void setSnsInfoUrl(String snsInfoUrl) {
		this.snsInfoUrl = snsInfoUrl;
	}
	public String getInfoUrl() {
		return infoUrl;
	}
	public void setInfoUrl(String infoUrl) {
		this.infoUrl = infoUrl;
	}
	public long getUid() {
		return uid;
	}
	public void setUid(long uid) {
		this.uid = uid;
	}
	
}
